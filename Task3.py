#–°—Ç—É–¥–µ–Ω—Ç –í–∞—Å—è –æ—á–µ–Ω—å –ª—é–±–∏—Ç –∫—É—Ä—Å ¬´–ö–æ–º–ø—å—é—Ç–µ—Ä–Ω–∞—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç—å¬ª. –û–¥–Ω–∞–∂–¥—ã –í–∞—Å–µ
#–∑–∞–¥–∞–ª–∏ –¥–æ–º–∞—à–Ω–µ–µ –∑–∞–¥–∞–Ω–∏–µ –∑–∞—à–∏—Ñ—Ä–æ–≤–∞—Ç—å –¥–∞–Ω–Ω—ã–µ, –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–µ –≤ —Å–æ–æ–±—â–µ–Ω–∏–µ. –ù–µ–¥–æ–ª–≥–æ
#–¥—É–º–∞—è, –í–∞—Å—è —Ä–µ—à–∏–ª –∑–∞–º–µ–Ω–∏—Ç—å –≤—Å–µ —Ü–µ–ª—ã–µ —á–∏—Å–ª–∞ –Ω–∞ —Ñ—É–Ω–∫—Ü–∏—é –æ—Ç —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞. –§—É–Ω–∫—Ü–∏—é
#–æ–Ω –ø—Ä–∏–¥—É–º–∞–ª –Ω–µ —Å–ª–æ–∂–Ω—É—é 4ùë•^2 ‚àí 7, –≥–¥–µ ùë• ‚àí –∏—Å—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ. –ü–æ–º–æ–≥–∏—Ç–µ –í–∞—Å–µ —Å –µ–≥–æ
#–¥–æ–º–∞—à–Ω–∏–º –∑–∞–¥–∞–Ω–∏–µ–º.

import re

test1 = "20 + 22 = 42"
test2 = "20 30 500 —Ç–µ–∫—Å—Ç —Ç–µ–∫—Å—Ç —Ç–µ–∫—Å—Ç"
test3 = "—Å–æ–æ–±—â–µ–Ω–∏–µ –±–µ–∑ —Ü–∏—Ñ—Ä"
test4 = "10 100 1000 - –ª—é–±—ã–µ —Ü–∏—Ñ—Ä—ã –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –¥–ª—è –∫–æ–¥–∏—Ä–æ–≤–∫–∏!"
test5 = "–ú–Ω–µ 18 –ª–µ—Ç"

testall = [test1, test2, test3, test4, test5]

for i in range(5):
    reg = re.findall(r'\d+', testall[i])
    #–Ω–∞—Ö–æ–¥–∏–º –≤—Å–µ —Ü–∏—Ñ—Ä—ã, –∫–æ—Ç–æ—Ä—ã–µ –Ω—É–∂–Ω–æ –±—É–¥–µ—Ç –∑–∞–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å
    if len(reg) == 0:
        print("–í —Å–æ–æ–±—â–µ–Ω–∏–∏ –Ω–µ—Ç —Å–∏–º–≤–æ–ª–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–Ω–æ –∑–∞–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å")
        #–µ—Å–ª–∏ –≤ reg –Ω–µ –¥–æ–±–∞–≤–∏–ª—Å—è –Ω–∏ –æ–¥–∏–Ω —ç–ª–µ–º–µ–Ω—Ç, –∑–Ω–∞—á–∏—Ç —Ü–∏—Ñ—Ä –Ω–µ –Ω–∞–π–¥–µ–Ω–æ
    else:
        for j in range(len(reg)):
            new = str(4 * int(reg[j])**2 - 7)
            #—Å—á–∏—Ç–∞–µ–º –Ω–æ–≤–æ–µ —á–∏—Å–ª–æ –ø–æ —Ñ–æ—Ä–º—É–ª–µ –∏–∑ —É—Å–ª–æ–≤–∏—è
            testall[i] = re.sub(reg[j], new, testall[i])
            #—Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ sub –∑–∞–º–µ–Ω—è–µ–º –∏–∑–Ω–∞—á–∞–ª—å–Ω—É—é —Ü–∏—Ñ—Ä—É –Ω–∞ –Ω–æ–≤—É—é –≤–æ –≤—Å–µ–º –¥–æ–∫—É–º–µ–Ω—Ç–µ
        print(testall[i])

